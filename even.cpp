/*
 * ============================================
 * Program: Print Even Numbers (2 to 30)
 * ============================================
 * Description: Prints all even numbers from 2 to 30 using a loop.
 * Purpose:     Learn for-loop and loop step (i += 2).
 * ============================================
 */

#include <iostream>

int main()
{
    // -----------------------------------------
    // Loop from 2 to 30, step by 2 (i += 2)
    // So we get: 2, 4, 6, 8, ... 30
    // -----------------------------------------
    for (int i = 2; i <= 30; i += 2)
    {
        std::cout << i << std::endl;
    }

    return 0;
}

